package com.hz.model;

public class HotelStory {
	
	private int id;
	private int hid;
	private String hname;
	private String gushi;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getHid() {
		return hid;
	}
	public void setHid(int hid) {
		this.hid = hid;
	}
	public String getHname() {
		return hname;
	}
	public void setHname(String hname) {
		this.hname = hname;
	}
	public String getGushi() {
		return gushi;
	}
	public void setGushi(String gushi) {
		this.gushi = gushi;
	}
	

}
